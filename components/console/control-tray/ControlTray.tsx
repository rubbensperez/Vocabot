import cn from 'classnames';

import { memo, ReactNode, useEffect, useState } from 'react';
import { AudioRecorder } from '../../../lib/audio-recorder';

import { useLiveAPIContext } from '../../../contexts/LiveAPIContext';
import { useUI } from '@/lib/state';

export type ControlTrayProps = {
  children?: ReactNode;
};

function ControlTray({ children }: ControlTrayProps) {
  const [audioRecorder] = useState(() => new AudioRecorder());
  const [muted, setMuted] = useState(false);

  const { showAgentEdit, showUserConfig } = useUI();
  const { client, connected, connect, disconnect } = useLiveAPIContext();

  // Stop the current agent if the user is editing the agent or user config
  useEffect(() => {
    if (showAgentEdit || showUserConfig) {
      if (connected) disconnect();
    }
  }, [showUserConfig, showAgentEdit, connected, disconnect]);

  useEffect(() => {
    const onData = (base64: string) => {
      client.sendRealtimeInput([
        {
          mimeType: 'audio/pcm;rate=16000',
          data: base64,
        },
      ]);
    };
    if (connected && !muted && audioRecorder) {
      audioRecorder.on('data', onData);
      audioRecorder.start();
    } else {
      audioRecorder.stop();
    }
    return () => {
      audioRecorder.off('data', onData);
    };
  }, [connected, client, muted, audioRecorder]);

  return (
    <section className="control-tray">
      <nav className="actions-nav">
        <button
          className={cn('action-button connect-toggle', { connected })}
          onClick={() => (connected ? disconnect() : connect())}
          aria-label={connected ? 'Stop session' : 'Start session'}
        >
          {connected ? (
            <span className="material-symbols-outlined filled">stop</span>
          ) : (
            <span className="material-symbols-outlined filled">play_arrow</span>
          )}
        </button>
        <button
          className={cn('action-button mic-button')}
          onClick={() => setMuted(!muted)}
          disabled={!connected}
          aria-label={muted ? 'Unmute microphone' : 'Mute microphone'}
        >
          {!muted ? (
            <span className="material-symbols-outlined filled">mic</span>
          ) : (
            <span className="material-symbols-outlined filled">mic_off</span>
          )}
        </button>
        {children}
      </nav>
    </section>
  );
}

export default memo(ControlTray);
